package cn.edu.fudan.se.helpseeking.bean;

public class NewTopicWebPagesInfo {
	//记录一个topic 对应的 webpage
	String webPagesId="";
	String topicId="";
	String webTitle="";
	String webSummary="";
	String webURL="";
	
	
	public String getWebPagesId() {
		return webPagesId;
	}
	public void setWebPagesId(String webPagesId) {
		this.webPagesId = webPagesId;
	}
	public String getTopicId() {
		return topicId;
	}
	public void setTopicId(String topicId) {
		this.topicId = topicId;
	}
	public String getWebTitle() {
		return webTitle;
	}
	public void setWebTitle(String webTitle) {
		this.webTitle = webTitle;
	}
	public String getWebSummary() {
		return webSummary;
	}
	public void setWebSummary(String webSummary) {
		this.webSummary = webSummary;
	}
	public String getWebURL() {
		return webURL;
	}
	public void setWebURL(String webURL) {
		this.webURL = webURL;
	}
	
	
	
	

}
